@ManagedBean(name="monitoringBeaCukaiBean")
@ViewScoped
public class MonitoringBeaCukaiBean implements Serializable{
    @EJB PlanningContLoadFacadeRemote planningContLoadFacadeRemote;
    List<Object[]> objects;

    @PostConstruct
    private void construct() {
        objects = planningContLoadFacadeRemote.findMonitoringCustoms();
    }

    public void postProcessXLS(Object document) {
        HSSFWorkbook wb = (HSSFWorkbook) document;
        HSSFSheet sheet = wb.getSheetAt(0);
        HSSFRow header = sheet.getRow(0);

        HSSFCellStyle cellStyle = wb.createCellStyle();
        cellStyle.setFillForegroundColor(HSSFColor.YELLOW.index);
        cellStyle.setFillPattern(HSSFCellStyle.SOLID_FOREGROUND);

        for (int i = 0; i < header.getPhysicalNumberOfCells(); i++) {
            HSSFCell cell = header.getCell(i);

            cell.setCellStyle(cellStyle);
        }
    }

    public List<Object[]> getObjects() {
        return objects;
    }

    public void setObjects(List<Object[]> objects) {
        this.objects = objects;
    }
    
}
